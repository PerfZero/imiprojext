import PublicHeader from '@/components/PublicHeader.vue';
import PublicFooter from '@/components/PublicFooter.vue';

declare global {
    interface Window {
        Swiper: any;
    }
}

onMounted(() => {
    nextTick(() => {
        if (window.Swiper) {
            const levelsSwiper = document.querySelector('.levels-swiper');
            if (levelsSwiper) {
                new window.Swiper(levelsSwiper, {
                    slidesPerView: 'auto',
                    spaceBetween: 15,
                    pagination: {
                        el: '.swiper-pagination',
                        clickable: true,
                    },
                    breakpoints: {
                        576: {
                            slidesPerView: 2,
                        },
                        768: {
                            slidesPerView: 3,
                        },
                        992: {
                            slidesPerView: 4,
                        },
                    },
                });
            }
        }
    });
});
</script>

